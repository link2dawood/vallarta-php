              <strong>THC</strong><br />
              Medical experts say that THC has psychoactive properties. In other words, THC is what produces the &ldquo;high&rdquo; effect that people tend to associate with using cannabis.<br />
              Strains of marijuana with a high THC content may be helpful for people with pain, difficulty sleeping, and depression, though they can make some people anxious.<br />
