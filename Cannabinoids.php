Cannabis ruderalis has its origins in cooler parts of the world, such as Russia and the border between Hungary and Ukraine. It grows wild, and some speculate that it may descend from escaped hemp plants.<br />
It does not have very high levels of THC or CBD, but breeders value it for its ability to flower by itself, without assistance from a cultivator. This is why people frequently use ruderalis to create hybrids with sativa or indica.<br />
