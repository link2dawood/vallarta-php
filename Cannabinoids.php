              <strong>Indica</strong><br />
              The Cannabis indica plant originated in the Middle East, in places such as Afghanistan, Pakistan, and Tibet.<br />
              It generally has a higher CBD content than Cannabis sativa, though the CBD to THC ratio is very close to 1:1<br />
              The general perception is that indica is an effective pain reliever, with a flat and relaxing high. Many medical marijuana strains contain a hybrid form of this strain.<br />
              That said, it is important to note that little scientific evidence backs these ideas. There are far more variations within the indica vs. sativa categorization, and many scientists believe<br />
that we should not generalize the psychoactive and other effects of different strains.<br />
<br />
<strong>Sativa</strong><br />
Cannabis sativa comes from warmer parts of the world, such as Southeast Asia and Central and South America.<br />
The general perception is that it provides a more energizing and creative high, though it can prompt anxiety in some people. Sativa can also be helpful for people with depression, headaches, nausea, and appetite loss. Sativa plants tend to contain more THC than CBD.<br />
Again, it is important to note that some scientific research negates these claims. Some sativa plants may be energizing while others may not. The same goes for indica strains.<br />
<br />
<strong>Hybrids</strong><br />
Both growers and nature have created hybrid forms of both Cannabis sativa and Cannabis indica throughout the long history of humans&rsquo; use of cannabis.<br />
People bred hybrids, for example, to make plants that grow more quickly, improve yield, and balance out the energizing and calming effects.<br />
<br />
