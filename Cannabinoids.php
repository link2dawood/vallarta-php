              <br />
              CBN. Cannabinol (CBN) is used to ease symptoms and side effects of neurological conditions, including epilepsy, seizures, and uncontrollable muscle stiffness.<br />
              <br />
              THCA. Tetrahydrocannabinol acid (THCA) is similar to THC, but it doesn&rsquo;t cause any psychoactive effects. Its potential benefits include reducing inflammation caused by arthritis and autoimmune diseases. It may also help reduce symptoms of neurological conditions like Parkinson&rsquo;s disease and ALS.<br />
