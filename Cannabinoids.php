              THC. THC is the main psychoactive compound in cannabis plants. It&rsquo;s responsible for the &ldquo;high&rdquo; or state of euphoria associated with cannabis use. Levels of THC have been increasing as growers try to create hybrids with a greater concentration of the compound.<br />
