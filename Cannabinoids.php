              <strong>CBD</strong><br />
              CBD does not create a &ldquo;high,&rdquo; but it can affect mood and be helpful in addressing anxiety and psychoses. However, despite its reputation for inducing calm, CBD can be a stimulant in small and monitored doses.<br />
