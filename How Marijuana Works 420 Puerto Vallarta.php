                  </strong>The brains of people who have smoked pot for many years may respond differently to certain rewards, compared with the brains of people who don't use the drug, according to a recent study. In the study, researchers wanted to see whether the brains of 59 chronic marijuana users would respond differently to the photos of objects used for smoking marijuana than they did to the photos of objects that are considered &quot;natural rewards,&quot; such as their favorite fruits.<br />
