                  I keep hearing about cannabis hybrids. What are they exactly?<br />
                  <br />
                  Hybrids, or hybrid strains, contain indica and sativa genetics. Indicas, known for their effects on the body, and sativas, known for their effects on the mind, can be combined to make a strain that provides physical and cerebral reactions. The best of both worlds, if you will.<br />
                  <br />
                  Hybrids fall into three categories: sativa dominant, indica dominant, or 50-50. Sativa dominants may be uplifting, but likely have less pronounced physical effects. Indica dominants typically have physical effects, like body relaxation, but may be more stimulating than an indica. A 50-50 hybrid has equal parts indica and sativa, and is balanced and calming.<br />
                  <br />
                  Some popular hybrid strains include Blue Dream, Pineapple Express, and Wedding Cake. As different as they are, all deliver mental and physical relief.
                  <br />
                  <br />
                  <strong>What is Hybrid Cannabis?<br />
                  </strong><br />
                  Hybrid cannabis strains are plants that have the combined characteristics of Cannabis sativa and Cannabis indica plants, depending on its genetic lineage. In fact, most cannabis today is a type of hybrid marked as either indica- or sativa-dominant, rather than pure indica or sativa. This is because &ldquo;landrace strains,&rdquo; or original cannabis strains that maintain their native characteristics, are hard to come by in today&rsquo;s cross-breeding, global market. <br />
                  <br />
                  Today&rsquo;s hybrid cannabis plants may be tall with bushier leaves, short with thin leaves, and have a variety of unique qualities that make it evident they are a more modern cultivar than a landrace strain. <br />
