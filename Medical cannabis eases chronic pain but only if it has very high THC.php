                <h3>Medical cannabis eases chronic pain &ndash; but only if it has very high THC                </h3>
            <p>Most prescribed cannabis products for chronic pain cause side effects like dizziness, but a new review has found that only those with very high THC, not CBD, actually provide pain relief<br />
              <br />
              Some orally ingested cannabis products can provide short-term relief of chronic pain, according to one of the largest-ever reviews of studies on their use for this purpose. Products with a high ratio of tetrahydrocannabinol (THC) to cannabidiol (CBD) seem to offer the most dramatic pain reduction, but relief often comes with side effects including nausea, dizziness and drowsiness.<br />
              <br />
              The use of cannabis for medical purposes is now legal in a growing list of countries around the world. In the US, 37 states have legalised medical marijuana and 19 now allow adults to purchase cannabis for recreational use.<br />
              <br />
              The most common reported use of medical marijuana in the US is for pain control, and it is seen as a welcome alternative to opioids. More than 20 per cent of adults in the US live with chronic pain, according to the Centers for Disease Control and Prevention.<br />
              <br />
              Yet despite the growing accessibility and popularity of cannabis products, research into their medicinal effects is limited. Several studies have suggested that medical cannabis can help alleviate pain. To determine whether these products could specifically reduce chronic pain &ndash; defined as pain persisting for three months or more &ndash; and how the ratio of THC to CBD impacted their efficacy, Marian McDonagh at Oregon Health &amp; Science University and her colleagues scoured previous research.<br />
              <br />
              &ldquo;We wanted to know, would cannabis for treating chronic pain have any kind of similar benefits [to opioids]? And of course, we really wanted to look deep into the adverse side effects side, particularly the more serious ones,&rdquo; says McDonagh.
              <br />
              <br />
              The researchers searched more than 3000 studies before identifying 25 that met their stringent criteria: they had to span at least four weeks and include people with different kinds of pain, such as back pain, chronic headaches and diabetic neuropathy, which causes burning and tingling. Of the 25 studies, 18 compared cannabis products against placebos. None of the studies included substances that could be purchased at recreational marijuana dispensaries.<br />
              <br />
              The analysis ultimately included data for more than 14,000 participants. It revealed that some cannabis products do provide minor to moderate pain reduction and that those with a high ratio of THC to CBD were most likely to subdue discomfort. THC provides marijuana&rsquo;s psychoactive effect or &ldquo;high&rdquo;, while CBD has been reported to ease symptoms of anxiety and inflammation, though its efficacy on its own is still under debate.<br />
              <br />
              Participants that used products containing at least 98 per cent THC reported a roughly 30 per cent reduction in pain symptoms. There was no significant improvement in those who consumed products with lower amounts of THC and higher amounts of CBD.<br />
              <br />
              &ldquo;There is so much noise out there about CBD really being able to treat pain,&rdquo; says McDonagh. &ldquo;This might help down the road to clarify whether that&rsquo;s true or not. Right now, there&rsquo;s just not enough evidence.&rdquo;<br />
              <br />
              The people that experienced the greatest benefits were those with neuropathic pain &ndash; the kind of nerve pain that usually causes tingling and burning sensations.<br />
              <br />
              The researchers noted that, while suggestive, the results were based on what limited evidence they could gather and that further studies are needed before medical cannabis can be recommended as a potential alternative to other prescription drugs for pain relief. And, because most of the trials the team reviewed spanned between one and six months, it is unclear if cannabis products can offer meaningful relief longer-term.<br />
              <br />
              McDonagh says the decision to use cannabis for pain should depend on what medications someone has already tried and what their particular concerns are. &ldquo;For an individual patient, it&rsquo;s best to start with your doctor to see what&rsquo;s going to work for you,&rdquo; she says.<br />
              Journal reference: Annals of Internal Medicine<br />
