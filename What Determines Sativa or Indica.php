                <h3><strong>What Determines Sativa or Indica?</strong></h3>
                <p><br />
                  Readers have already learned that the sometimes pungent aromas produced by many strains of cannabis are the result of terpenes, the special molecules in the herb that are similar in many respects to cannabinoids like THC and CBD.<br />
While many might guess that a particular strain of cannabis is categorized as indica, sativa, or hybrid based on the presence or absence of a particular cannabinoid, or a cannabinoid in a particular volume, it is actually a terpene that determines this important status of a strain.<br />
<br />
Myrcene, the most common terpene in cannabis, is known to help patients sleep, battling conditions like anxiety and insomnia. If present in a specific strain in a volume greater than 0.5 percent, the strain is considered an indica. If the amount of myrcene is under one half of one percent, then the strain is deemed a sativa.<br />
<br />
This dynamic is a good example of the entourage effect, a theory that cannabinoids and terpenes work together in harmony to deliver medicinal efficacy to patients. Many terpenes buffer or enhance the effects of major cannabinoids like THC.<br />
